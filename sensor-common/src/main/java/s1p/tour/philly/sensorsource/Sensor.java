package s1p.tour.philly.sensorsource;

public class Sensor {

	private int id;
	private double temperature;

	public Sensor() {
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public double getTemperature() {
		return temperature;
	}

	public void setTemperature(double temperature) {
		this.temperature = temperature;
	}
}
